from .base import BaseAgent
from .prompts import get_agent_prompt_with_examples


class SalesAgent(BaseAgent):
    """
    Агент отдела продаж с экспертизой в консультативных продажах и подборе тарифов.

    Использует few-shot примеры для эффективного взаимодействия с клиентами и презентации услуг.
    """

    def __init__(self) -> None:
        """
        Инициализация агента продаж с промптом и примерами.

        Создает экземпляр агента продаж с экспертными знаниями
        в области консультативных продаж и клиентского сервиса.
        Интегрирует few-shot примеры для повышения эффективности продаж.
        """
        system_prompt = get_agent_prompt_with_examples("sales", "SYSTEM_PROMPT")
        super().__init__("sales", system_prompt)

    def get_capabilities(self) -> list[str]:
        """
        Возвращает список возможностей агента продаж.

        Описывает коммерческие компетенции агента, включая
        консультирование, продажи и работу с клиентами.

        Returns
        -------
            Список строк с описанием продажных компетенций

        """
        return [
            "Консультации по тарифам и услугам",
            "Подбор оптимальных решений",
            "Кросс-продажи и апсейлы",
            "Работа с возражениями",
            "Оформление заявок и договоров",
        ]
