from .base import BaseAgent
from .prompts import get_agent_prompt_with_examples


class TechSupportAgent(BaseAgent):
    """
    Агент технической поддержки с экспертизой в диагностике и решении технических проблем.

    Использует few-shot примеры для эффективного решения типичных технических вопросов.
    """

    def __init__(self) -> None:
        """
        Инициализация агента техподдержки с промптом и примерами.

        Создает экземпляр агента технической поддержки с экспертными
        знаниями в области диагностики и решения технических проблем.
        Интегрирует few-shot примеры для улучшения качества ответов.
        """
        system_prompt = get_agent_prompt_with_examples("tech_support", "SYSTEM_PROMPT")
        super().__init__("tech_support", system_prompt)

    def get_capabilities(self) -> list[str]:
        """
        Возвращает список технических возможностей агента.

        Описывает экспертные области технической поддержки,
        включая диагностику, настройку и решение проблем.

        Returns
        -------
            Список строк с описанием технических компетенций

        """
        return [
            "Диагностика технических проблем",
            "Настройка оборудования и ПО",
            "Восстановление доступов и паролей",
            "Устранение ошибок и сбоев",
            "Пошаговые инструкции",
            "Консультации по использованию",
            "Решение проблем подключения",
        ]
